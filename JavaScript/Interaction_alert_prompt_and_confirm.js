// Interaction: alert, prompt, confirm

// alert
// syntax:
alert(message); // this shows a message and pauses script execution until the user presses "OK"
// the mini-window with the message is called a modal window.
// the word "modal" means that the visitor can't interact with the rest of the page, press other buttons e.t.c., until they have dealts with the window



// prompt
// the function prompt accepts two arguments:
prompt(title);

// it shows a modal window with a text message, an input field for the visitor and the buttons OK/Cancel
// title - is the text to show the visitor
// default - an optional second parameter, the initial value for the input field
